def interpolate_sensitivity(sigma, ref_low, ref_high, val_low, val_high):
    """Interpolate (or extrapolate) the abundance sensitivity for a given sigma.

    sigma in [0, ref_low]       → scale linearly from zero through val_low
    sigma in [ref_low, ref_high] → linear interpolation between the two table entries
    sigma > ref_high             → linear extrapolation beyond ref_high
    """
    if sigma <= 0:
        return 0.0
    if sigma <= ref_low:
        return val_low * (sigma / ref_low)
    if sigma <= ref_high:
        t = (sigma - ref_low) / (ref_high - ref_low)
        return val_low + t * (val_high - val_low)
    slope = (val_high - val_low) / (ref_high - ref_low)
    return val_high + slope * (sigma - ref_high)


def get_uncertainty_atmospheric(temperature, sigma_teff, sigma_logg, sigma_metallicity, sigma_xi, element):
    temperatures = np.array(list(temperature_data.keys()))
    closest_temp = temperatures[np.abs(temperatures - temperature).argmin()]
    df = temperature_data[closest_temp]

    sigma_teff = abs(float(sigma_teff))
    sigma_logg = abs(float(sigma_logg))
    sigma_metallicity = abs(float(sigma_metallicity))
    sigma_xi = abs(float(sigma_xi))

    def compute_for_row(row):
        u_teff = interpolate_sensitivity(
            sigma_teff, 50, 100,
            float(row["+50_Teff"]), float(row["+100_Teff"])
        )
        u_logg = interpolate_sensitivity(
            sigma_logg, 0.10, 0.20,
            float(row["+0.10_logg"]), float(row["+0.20_logg"])
        )
        u_met = interpolate_sensitivity(
            sigma_metallicity, 0.10, 0.20,
            float(row["+0.10_FeH"]), float(row["+0.20_FeH"])
        )
        u_xi = interpolate_sensitivity(
            sigma_xi, 0.25, 0.50,
            float(row["+0.25_xi"]), float(row["+0.50_xi"])
        )
        rms = np.sqrt(u_teff**2 + u_logg**2 + u_met**2 + u_xi**2)
        return u_teff, u_logg, u_met, u_xi, rms

    if element.lower() == 'all':
        results = {}
        for _, row in df.iterrows():
            elem = row["Element"]
            u_teff, u_logg, u_met, u_xi, rms = compute_for_row(row)
            results[elem] = {
                "sigma(Teff)":   round(u_teff, 4),
                "sigma(logg)":   round(u_logg, 4),
                "sigma([Fe/H])": round(u_met,  4),
                "sigma(xi)":     round(u_xi,   4),
                "sigma(atm)":    round(float(rms), 4),
            }
        return results

    row = df[df["Element"] == element]
    if row.empty:
        return {"Erro": f"Element '{element}' not found for Teff = {closest_temp} K."}

    u_teff, u_logg, u_met, u_xi, rms = compute_for_row(row.iloc[0])

    return {
        "sigma(Teff)  contribution": round(u_teff, 4),
        "sigma(logg)  contribution": round(u_logg, 4),
        "sigma([Fe/H]) contribution": round(u_met,  4),
        "sigma(xi)    contribution": round(u_xi,   4),
        "sigma(atm) [quadratic sum]": round(float(rms), 4),
    }

